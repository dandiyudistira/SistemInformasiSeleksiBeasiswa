    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        String objButton[]={"yes","no"};
        int PromptResult = JOptionPane.showOptionDialog(null, "Apakah anda ingin menghapus data ini..?","Message",
                JOptionPane.DEFAULT_OPTION,JOptionPane.QUESTION_MESSAGE, null, objButton, objButton[1]);
        if(PromptResult==0){
           Kelas.inputdata idm = new Kelas.inputdata();
           idm.nim=txt_nim.getText();
           try{
                idm.hapus();
                JOptionPane.showMessageDialog(null, "Berhasil dihapus");
                bersih();
                viewdata();
                tombol(false);
                btn_simpan.setEnabled(true);
            }
           catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

