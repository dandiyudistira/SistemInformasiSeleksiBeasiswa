    void filterangka(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Kolom Ini Hanya Bisa Memasukan Karakter Angka");
        }
    }
    void filterhuruf(KeyEvent b){
        if(Character.isDigit(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,"Kolom Ini Hanya Bisa Memasukan Karakter Huruf");
        }
    }
